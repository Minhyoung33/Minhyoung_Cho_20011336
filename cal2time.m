function [time] = cal2time(cal)
% ==============================================================================
% CAL2TIME Convert calendar date and time to standard time
% ------------------------------------------------------------------------------
% Inputs:
%   dt <n x 6 matrix> : Calendar date and time (year/month/day/hour/min/sec)
% ------------------------------------------------------------------------------
% Outputs:
%   time <n x 1 matrix> : Standard time [s]
% ------------------------------------------------------------------------------
% References:
%   -
% ------------------------------------------------------------------------------
% Author:
%   Cheolsoon Lim (csleem@sju.ac.kr)
% ------------------------------------------------------------------------------
% History:
%   -
% ==============================================================================

if size(cal, 2) > 6 || size(cal, 2) < 3
    time = [];
else    
    time = (datenum(cal(:,1:3)) - datenum([1970, 1, 1]))*86400;
    time = time + cal(:,4)*3600 + cal(:,5)*60 + cal(:,6);
end